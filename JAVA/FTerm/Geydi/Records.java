public class Records {
    
    private double wr;
    private double or;

    public Records(double wr, double or) {
        this.wr = wr;
        this.or = or;
    }

    public double getWr() {
        return wr;
    }

    public void setWr(double wr) {
        this.wr = wr;
    }

    public double getOr() {
        return or;
    }

    public void setOr(double or) {
        this.or = or;
    }
    
    
    
}
