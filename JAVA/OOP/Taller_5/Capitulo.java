public class Capitulo {
    String titulo;
    int paginaInicio;
    int paginaFin;
    int numero;

    public Capitulo(String titulo, int paginaInicio, int paginaFin, int numero) {
        this.titulo = titulo;
        this.paginaInicio = paginaInicio;
        this.paginaFin = paginaFin;
        this.numero = numero;
    } 
}